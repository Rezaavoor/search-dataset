def _detect_provider(provider: str) -> str:
    """Resolve 'auto' provider to a concrete provider name."""
    if provider != "auto":
        return provider
    if os.environ.get("AZURE_OPENAI_API_KEY") and os.environ.get("AZURE_OPENAI_ENDPOINT"):
        return "azure"
    if os.environ.get("OPENAI_API_KEY"):
        return "openai"
    if os.environ.get("AWS_ACCESS_KEY_ID") or os.environ.get("AWS_BEDROCK_REGION"):
        return "bedrock"
    raise ValueError(
        "No API credentials found. Set OPENAI_API_KEY, Azure vars, "
        "or AWS_ACCESS_KEY_ID + AWS_BEDROCK_REGION."
    )


def _is_cohere_model(model: str) -> bool:
    """Check if the model ID refers to a Cohere embedding model."""
    m = model.lower()
    return "cohere" in m or m.startswith("eu.cohere") or m.startswith("us.cohere")


def setup_embedding_model(model: str, provider: str = "auto"):
    """Set up an embedding model for **document** embedding.

    For Bedrock Cohere models this uses ``input_type="search_document"``,
    which is the correct mode for indexing corpus pages.

    Providers:
      - openai: Uses OPENAI_API_KEY
      - azure: Uses AZURE_OPENAI_* vars
      - bedrock: Uses AWS credentials (AWS_ACCESS_KEY_ID/SECRET or default profile)
      - auto: Detects from env vars
    """
    provider = _detect_provider(provider)

    if provider == "azure":
        from langchain_openai import AzureOpenAIEmbeddings

        endpoint = os.environ.get("AZURE_OPENAI_ENDPOINT")
        api_key = os.environ.get("AZURE_OPENAI_API_KEY")
        api_version = os.environ.get("AZURE_OPENAI_API_VERSION", "2024-02-15-preview")
        print(f"  Provider: Azure OpenAI (deployment: {model})")
        return AzureOpenAIEmbeddings(
            azure_endpoint=endpoint,
            api_key=api_key,
            api_version=api_version,
            azure_deployment=model,
        )
    elif provider == "bedrock":
        import boto3
        from langchain_aws.embeddings import BedrockEmbeddings

        region = os.environ.get("AWS_BEDROCK_REGION", "eu-central-1")
        client = boto3.client("bedrock-runtime", region_name=region)
        print(f"  Provider: AWS Bedrock (model: {model}, region: {region})")
        return BedrockEmbeddings(
            client=client,
            model_id=model,
        )
    else:
        from langchain_openai import OpenAIEmbeddings

        print(f"  Provider: OpenAI (model: {model})")
        return OpenAIEmbeddings(model=model)


def setup_query_embedding_model(model: str, provider: str = "auto"):
    """Set up an embedding model for **query** embedding.

    For Bedrock Cohere models this configures ``input_type="search_query"``
    so that queries receive the correct asymmetric special tokens.
    For non-Cohere models this returns the same model as
    ``setup_embedding_model`` (symmetric models don't distinguish).
    """
    provider = _detect_provider(provider)

    if provider == "bedrock" and _is_cohere_model(model):
        import boto3
        from langchain_aws.embeddings import BedrockEmbeddings

        region = os.environ.get("AWS_BEDROCK_REGION", "eu-central-1")
        client = boto3.client("bedrock-runtime", region_name=region)
        print(f"  Query embedder: AWS Bedrock (model: {model}, input_type: search_query)")
        return BedrockEmbeddings(
            client=client,
            model_id=model,
            model_kwargs={"input_type": "search_query"},
        )

    # For OpenAI / Azure / non-Cohere Bedrock: symmetric — same model works
    return setup_embedding_model(model, provider)


# ============================================================================
# Corpus embedding computation (for new models)
# ============================================================================
def _embed_batch_with_retry(
    embedding_model,
    texts: List[str],
    *,
    max_retries: int = 6,
    base_delay: float = 5.0,
) -> List[Optional[List[float]]]:
    """Embed a batch of texts with exponential backoff on throttling errors."""
    for attempt in range(max_retries):
        try:
            batch_vecs = embedding_model.embed_documents(texts)
            if isinstance(batch_vecs, list) and len(batch_vecs) == len(texts):
                return batch_vecs
            raise ValueError("Unexpected batch result shape")
        except Exception as e:
            err_str = str(e).lower()
            is_throttle = "throttl" in err_str or "rate" in err_str or "too many" in err_str
            if is_throttle and attempt < max_retries - 1:
                delay = base_delay * (2 ** attempt)
                print(f"  Throttled, retrying in {delay:.0f}s (attempt {attempt + 2}/{max_retries})...")
                time.sleep(delay)
                continue
            # Last attempt or non-throttle error: fall back to per-text
            results: List[Optional[List[float]]] = []
            for t in texts:
                for sub_attempt in range(max_retries):
                    try:
                        v = embedding_model.embed_query(t)
                        results.append(v if isinstance(v, list) else None)
                        break
                    except Exception as sub_e:
                        sub_err = str(sub_e).lower()
                        if ("throttl" in sub_err or "rate" in sub_err or "too many" in sub_err) and sub_attempt < max_retries - 1:
                            time.sleep(base_delay * (2 ** sub_attempt))
                            continue
                        results.append(None)
                        break
            return results
    return [None] * len(texts)


def compute_and_store_corpus_embeddings(
    conn,
    model_name: str,
    embedding_model,
    *,
    batch_size: int = 16,
) -> int:
    """Compute embeddings for all pages missing the model's column and store them.

    Uses small batches + exponential backoff to handle API throttling.
    """
    ensure_embedding_columns(conn, model_name)
    blob_col = _emb_col(model_name)

    rows = conn.execute(
        f"""
        SELECT id, doc_content FROM pdf_page_store
        WHERE {blob_col} IS NULL
        ORDER BY rel_path, page_number
        """
    ).fetchall()

    if not rows:
        return 0

    ids = [int(r[0]) for r in rows]
    texts = [str(r[1] or "") for r in rows]
    total = len(texts)

    print(f"  Computing embeddings for {total} pages (batch_size={batch_size})...")

    stored = 0
    processed = 0
    for batch_ids, batch_texts in zip(
        iter_batched(ids, batch_size=batch_size),
        iter_batched(texts, batch_size=batch_size),
    ):
        vecs = _embed_batch_with_retry(embedding_model, batch_texts)

        # Store immediately after each batch (resume-safe)
        with conn:
            for row_id, v in zip(batch_ids, vecs):
                if not isinstance(v, list) or not v:
                    continue
                arr = np.asarray(v, dtype=np.float32)
                store_page_embedding(
                    conn, model_name, row_id=row_id, embedding=arr,
                )
                stored += 1

        processed += len(batch_texts)
        if processed % (batch_size * 10) == 0 or processed == total:
            print(f"  Embedded {processed}/{total} pages (stored: {stored})")

    return stored


# ============================================================================
# Search
# ============================================================================
def cosine_similarity_search(
    query_embedding: np.ndarray,
    corpus_keys: List[Tuple[str, int]],
    corpus_matrix: np.ndarray,
    corpus_norms: np.ndarray,
) -> List[Tuple[Tuple[str, int], float]]:
    """Return all corpus pages ranked by cosine similarity to the query."""
    q = query_embedding.astype(np.float32)
    qn = np.linalg.norm(q)
    if qn == 0:
        return [(k, 0.0) for k in corpus_keys]
    q = q / qn

    sims = corpus_matrix @ q  # (N,) dot products with pre-normalized corpus
    # Handle zero-norm pages
    sims = np.where(corpus_norms > 0, sims, 0.0)

    order = np.argsort(sims)[::-1]
    return [(corpus_keys[int(i)], float(sims[int(i)])) for i in order]


# ============================================================================
# Scoring
# ============================================================================
def _dcg_at_k(relevances: List[int], k: int) -> float:
    """Compute Discounted Cumulative Gain at rank k."""
    dcg = 0.0
    for i, rel in enumerate(relevances[:k]):
        dcg += rel / np.log2(i + 2)  # i is 0-indexed, so rank = i+1, log2(rank+1) = log2(i+2)
    return dcg


def compute_metrics(
    ranked: List[Tuple[str, float]],
    positives: set,
    hard_negatives: set,
    top_k_values: List[int],
) -> Dict[str, Any]:
    """Compute retrieval metrics for a single query."""
    results: Dict[str, Any] = {}

    # Find ranks of positives (1-indexed)
    positive_ranks: List[int] = []
    for rank_idx, (page_key, _score) in enumerate(ranked):
        if page_key in positives:
            positive_ranks.append(rank_idx + 1)

    first_positive_rank = positive_ranks[0] if positive_ranks else None

    # Recall@K
    for k in top_k_values:
        top_k_keys = {page_key for page_key, _ in ranked[:k]}
        hit = 1 if positives & top_k_keys else 0
        results[f"recall@{k}"] = hit

    # Reciprocal Rank (unbounded)
    results["reciprocal_rank"] = (1.0 / first_positive_rank) if first_positive_rank else 0.0

    # MRR@K — reciprocal rank truncated at K (0 if first positive is beyond K)
    for k in top_k_values:
        if first_positive_rank is not None and first_positive_rank <= k:
            results[f"mrr@{k}"] = 1.0 / first_positive_rank
        else:
            results[f"mrr@{k}"] = 0.0

    # Average Precision
    num_relevant = 0
    precision_sum = 0.0
    for rank_idx, (page_key, _score) in enumerate(ranked):
        if page_key in positives:
            num_relevant += 1
            precision_sum += num_relevant / (rank_idx + 1)
    results["average_precision"] = (
        (precision_sum / len(positives)) if positives else 0.0
    )

    # nDCG@K — binary relevance
    # Build relevance vector for as many ranks as we need
    max_k = max(top_k_values)
    relevances = [
        1 if page_key in positives else 0
        for page_key, _ in ranked[:max_k]
    ]
    # Ideal relevance: all positives at the top
    n_pos = len(positives)
    for k in top_k_values:
        dcg = _dcg_at_k(relevances, k)
        ideal_rels = [1] * min(n_pos, k) + [0] * max(0, k - n_pos)
        idcg = _dcg_at_k(ideal_rels, k)
        results[f"ndcg@{k}"] = (dcg / idcg) if idcg > 0 else 0.0

    # Hard negative analysis
    hard_neg_ranks: List[int] = []
    for rank_idx, (page_key, _score) in enumerate(ranked):
        if page_key in hard_negatives:
            hard_neg_ranks.append(rank_idx + 1)

    results["first_positive_rank"] = first_positive_rank
    results["hard_negative_ranks"] = hard_neg_ranks
    hn_outranks = False
    if hard_neg_ranks and first_positive_rank:
        hn_outranks = min(hard_neg_ranks) < first_positive_rank
    results["hard_neg_outranks_positive"] = hn_outranks

    return results


def parse_page_ref(ref: str) -> Optional[Tuple[str, int]]:
    """Parse 'filename.pdf (page N)' into (filename, page_number)."""
    ref = ref.strip()
    if " (page " in ref and ref.endswith(")"):
        parts = ref.rsplit(" (page ", 1)
        filename = parts[0].strip()
        try:
            page = int(parts[1].rstrip(")"))
            return (filename, page)
        except ValueError:
            return None
    return None


