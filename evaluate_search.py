def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Evaluate embedding model search quality using a synthetic dataset"
    )
    parser.add_argument(
        "--dataset", type=str, required=True,
        help="Path to the generated dataset CSV",
    )
    parser.add_argument(
        "--embedding-model", type=str, required=True,
        help="Embedding model name (e.g., text-embedding-3-large)",
    )
    parser.add_argument(
        "--top-k", type=int, nargs="+", default=[1, 5, 10, 20],
        help="K values for Recall@K (default: 1 5 10 20)",
    )
    parser.add_argument(
        "--pdf-store-db", type=str, default=None,
        help="SQLite DB path (default: processed/pdf_page_store.sqlite)",
    )
    parser.add_argument(
        "--provider", type=str,
        choices=["auto", "openai", "azure", "bedrock"],
        default="auto",
        help="Embedding provider (default: auto-detect from env vars)",
    )
    parser.add_argument(
        "--output", type=str, default=None,
        help="Output JSON path (default: output/eval_{model}.json)",
    )
    return parser


def main() -> int:
    parser = build_parser()
    args = parser.parse_args()

    model_name = args.embedding_model
    top_k_values = sorted(set(args.top_k))

    print("=" * 60)
    print("Embedding Search Evaluation")
    print("=" * 60)
    print(f"  Model: {model_name}")
    print(f"  Top-K: {top_k_values}")

    # --- Load dataset ---
    dataset_path = Path(args.dataset)
    if not dataset_path.exists():
        print(f"\nError: Dataset not found: {dataset_path}")
        return 1

    print(f"\n[1/5] Loading dataset: {dataset_path.name}")
    df = load_dataset(dataset_path)
    print(f"  Loaded {len(df)} queries")

    # --- Open SQLite store ---
    if args.pdf_store_db:
        db_path = Path(args.pdf_store_db).expanduser().resolve()
    else:
        db_path = SCRIPT_DIR / "processed" / "pdf_page_store.sqlite"
    if not db_path.exists():
        print(f"\nError: SQLite store not found: {db_path}")
        return 1

    conn = open_pdf_page_store(db_path)
    init_pdf_page_store(conn)
    print(f"  SQLite store: {db_path}")

    # --- Load or compute corpus embeddings ---
    print(f"\n[2/5] Loading corpus embeddings for: {model_name}")

    # Check if model column exists and has data
    blob_col = _emb_col(model_name)
    try:
        row = conn.execute(
            f"SELECT COUNT(*) FROM pdf_page_store WHERE {blob_col} IS NOT NULL"
        ).fetchone()
        existing_count = int(row[0]) if row else 0
    except Exception:
        existing_count = 0

    total_pages = int(
        conn.execute("SELECT COUNT(*) FROM pdf_page_store").fetchone()[0]
    )

    if existing_count < total_pages:
        missing = total_pages - existing_count
        print(f"  {existing_count}/{total_pages} pages have embeddings, {missing} missing")
        print(f"  Computing embeddings for missing pages...")
        emb_model = setup_embedding_model(model_name, args.provider)
        stored = compute_and_store_corpus_embeddings(conn, model_name, emb_model)
        print(f"  Stored {stored} new embeddings")
    else:
        print(f"  All {existing_count} pages have embeddings (cached)")
        emb_model = None  # Not needed if all cached

    # Load all embeddings into memory
    all_embeddings = load_page_embeddings(conn, model_name)
    print(f"  Loaded {len(all_embeddings)} page embeddings into memory")

    if not all_embeddings:
        print("\nError: No embeddings available. Cannot evaluate.")
        return 1

    # Build corpus index: list of keys + normalized matrix
    corpus_keys = list(all_embeddings.keys())
    corpus_matrix = np.stack([all_embeddings[k] for k in corpus_keys])  # (N, D)
    corpus_norms = np.linalg.norm(corpus_matrix, axis=1, keepdims=True)
    corpus_norms = np.where(corpus_norms > 0, corpus_norms, 1.0)
    corpus_matrix_normed = corpus_matrix / corpus_norms
    corpus_norms_flat = corpus_norms.flatten()

    # Build filename lookup: (filename, page) -> index into corpus
    filename_to_corpus: Dict[Tuple[str, int], int] = {}
    for idx, (rel_path, page_num) in enumerate(corpus_keys):
        filename = os.path.basename(rel_path)
        filename_to_corpus[(filename, page_num)] = idx

    # --- Embed queries ---
    print(f"\n[3/5] Embedding {len(df)} queries")
    queries = df["user_input"].tolist()

    # Use a query-specific embedding model so that asymmetric models
    # (e.g., Cohere) receive the correct input_type="search_query".
    query_emb_model = setup_query_embedding_model(model_name, args.provider)

    try:
        query_embeddings = query_emb_model.embed_documents(queries)
        print(f"  Embedded {len(queries)} queries (batched)")
    except Exception as e:
        print(f"  Batch embedding failed ({e}), falling back to per-query...")
        query_embeddings = []
        for q in queries:
            try:
                query_embeddings.append(query_emb_model.embed_query(q))
            except Exception:
                query_embeddings.append(None)

    # --- Search and score ---
    print(f"\n[4/5] Searching and scoring")
    per_query_results: List[Dict[str, Any]] = []
    max_k = max(top_k_values)

    for row_idx, (_, row) in enumerate(df.iterrows()):
        query = row["user_input"]
        q_emb = query_embeddings[row_idx]
        if q_emb is None:
            per_query_results.append({
                "query": query, "error": "embedding_failed",
            })
            continue

        q_vec = np.asarray(q_emb, dtype=np.float32)

        # Parse ground truth positives
        raw_positives = parse_json_col(row.get("source_files_with_pages"))
        positives: set = set()
        for ref in raw_positives:
            parsed = parse_page_ref(ref)
            if parsed:
                positives.add(parsed)

        # Parse hard negatives
        raw_hard_negs = parse_json_col(row.get("hard_negatives", "[]"))
        hard_negatives: set = set()
        for ref in raw_hard_negs:
            parsed = parse_page_ref(ref)
            if parsed:
                hard_negatives.add(parsed)

        if not positives:
            per_query_results.append({
                "query": query, "error": "no_positives_parsed",
                "raw_positives": raw_positives,
            })
            continue

        # Search
        ranked_with_scores = cosine_similarity_search(
            q_vec, corpus_keys, corpus_matrix_normed, corpus_norms_flat,
        )
        # Convert (rel_path, page) keys to (filename, page) for matching
        ranked_filename: List[Tuple[Tuple[str, int], float]] = []
        for (rel_path, page_num), score in ranked_with_scores:
            fn = os.path.basename(rel_path)
            ranked_filename.append(((fn, page_num), score))

        ranked_keys = [(k, s) for k, s in ranked_filename]

        metrics = compute_metrics(
            ranked_keys, positives, hard_negatives, top_k_values,
        )

        # Build readable top-K list
        top_k_retrieved = []
        for (fn, pn), score in ranked_filename[:max_k]:
            top_k_retrieved.append(f"{fn} (page {pn})")

        result: Dict[str, Any] = {
            "query": query,
            "positives": [f"{fn} (page {pn})" for fn, pn in sorted(positives)],
            "hard_negatives": [f"{fn} (page {pn})" for fn, pn in sorted(hard_negatives)],
            f"top_{max_k}_retrieved": top_k_retrieved,
            **metrics,
        }
        per_query_results.append(result)

    # --- Aggregate ---
    print(f"\n[5/5] Aggregating results")

    valid_results = [r for r in per_query_results if "error" not in r]
    n_valid = len(valid_results)

    if n_valid == 0:
        print("\nError: No valid query results to aggregate.")
        return 1

    # Aggregate metrics
    agg_metrics: Dict[str, float] = {}
    for k in top_k_values:
        agg_metrics[f"recall@{k}"] = sum(r[f"recall@{k}"] for r in valid_results) / n_valid

    agg_metrics["mrr"] = sum(r["reciprocal_rank"] for r in valid_results) / n_valid
    for k in top_k_values:
        agg_metrics[f"mrr@{k}"] = sum(r[f"mrr@{k}"] for r in valid_results) / n_valid

    agg_metrics["map"] = sum(r["average_precision"] for r in valid_results) / n_valid

    for k in top_k_values:
        agg_metrics[f"ndcg@{k}"] = sum(r[f"ndcg@{k}"] for r in valid_results) / n_valid

    # Hard negative analysis
    results_with_hn = [r for r in valid_results if r.get("hard_negative_ranks")]
    n_with_hn = len(results_with_hn)
    all_hn_ranks = []
    hn_outranks_count = 0
    for r in results_with_hn:
        all_hn_ranks.extend(r["hard_negative_ranks"])
        if r.get("hard_neg_outranks_positive"):
            hn_outranks_count += 1

    hn_analysis: Dict[str, Any] = {
        "queries_with_hard_negatives": n_with_hn,
        "mean_hard_negative_rank": (
            float(np.mean(all_hn_ranks)) if all_hn_ranks else None
        ),
        "hard_neg_outranks_positive_pct": (
            hn_outranks_count / n_with_hn if n_with_hn > 0 else 0.0
        ),
    }

    # Dataset quality diagnostics
    positive_set_sizes = []
    for r in valid_results:
        pos = r.get("positives", [])
        positive_set_sizes.append(len(pos) if isinstance(pos, list) else 0)

    quality_warnings: List[str] = []
    if n_valid < 50:
        quality_warnings.append(
            f"Small query set (n={n_valid}). Results may have high variance. "
            f"Consider generating >= 100 queries."
        )
    large_pos = sum(1 for s in positive_set_sizes if s > 50)
    if large_pos > 0:
        quality_warnings.append(
            f"{large_pos}/{n_valid} queries have >50 positive pages. "
            f"This may indicate boilerplate contamination in ground truth. "
            f"Run: python refine_dataset.py --dataset <your_csv> --analyze-only"
        )
    huge_pos = sum(1 for s in positive_set_sizes if s > 100)
    if huge_pos > 0:
        quality_warnings.append(
            f"{huge_pos}/{n_valid} queries have >100 positive pages — likely "
            f"inflated by shared headers/footers. Use refine_dataset.py to fix."
        )

    dataset_quality: Dict[str, Any] = {
        "positive_set_sizes": {
            "min": int(min(positive_set_sizes)) if positive_set_sizes else 0,
            "max": int(max(positive_set_sizes)) if positive_set_sizes else 0,
            "mean": float(np.mean(positive_set_sizes)) if positive_set_sizes else 0.0,
            "median": float(np.median(positive_set_sizes)) if positive_set_sizes else 0.0,
        },
        "queries_with_large_positives_gt50": large_pos,
        "queries_with_huge_positives_gt100": huge_pos,
        "warnings": quality_warnings,
    }

    # Print summary
    print(f"\n{'=' * 60}")
    print(f"Results: {model_name}")
    print(f"{'=' * 60}")
    print(f"  Corpus pages: {len(all_embeddings)}")
    print(f"  Queries evaluated: {n_valid}")
    print()
    for key, val in agg_metrics.items():
        print(f"  {key}: {val:.4f}")
    print()
    if n_with_hn > 0:
        print(f"  Hard negative analysis ({n_with_hn} queries with hard negatives):")
        if all_hn_ranks:
            print(f"    Mean hard negative rank: {np.mean(all_hn_ranks):.1f}")
        print(f"    Hard neg outranks positive: {hn_analysis['hard_neg_outranks_positive_pct']:.1%}")

    # Print dataset quality info
    print()
    print(f"  Dataset quality:")
    print(f"    Positive set sizes: min={dataset_quality['positive_set_sizes']['min']}, "
          f"max={dataset_quality['positive_set_sizes']['max']}, "
          f"mean={dataset_quality['positive_set_sizes']['mean']:.1f}, "
          f"median={dataset_quality['positive_set_sizes']['median']:.1f}")
    if quality_warnings:
        print()
        for w in quality_warnings:
            print(f"  ⚠ WARNING: {w}")
    print(f"{'=' * 60}")

    # Build output
    output_data = {
        "model": model_name,
        "corpus_pages": len(all_embeddings),
        "num_queries": n_valid,
        "top_k_values": top_k_values,
        "metrics": agg_metrics,
        "hard_negative_analysis": hn_analysis,
        "dataset_quality": dataset_quality,
        "per_query": per_query_results,
    }

    # Save JSON
    if args.output:
        output_path = Path(args.output)
    else:
        OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
        output_path = OUTPUT_DIR / f"eval_{sanitize_model_name(model_name)}.json"

    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(output_data, f, ensure_ascii=False, indent=2)
    print(f"\n  Results saved to: {output_path}")

    conn.close()
    return 0


if __name__ == "__main__":
    sys.exit(main())
